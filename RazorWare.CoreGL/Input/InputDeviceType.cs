namespace RazorWare.CoreGL.Input {
   //
   // Summary:
   //     The type of the input device.
   public enum InputDeviceType {
      //
      // Summary:
      //     Device is a keyboard.
      Keyboard = 0,
      //
      // Summary:
      //     Device is a mouse.
      Mouse = 1,
      //
      // Summary:
      //     Device is a Human Interface Device. Joysticks, joypads, pens and some specific
      //     usb keyboards/mice fall into this category.
      Hid = 2
   }
}
